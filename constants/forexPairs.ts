export const getSymbolForCurrency = (code: string): string => {
  const currency = currencies.find(c => c.code === code);
  return currency ? currency.symbol : code;
};

export const getPipValue = (pairCode: string): number => {
  const pair = allPairs.find(p => p.code === pairCode);
  return pair ? pair.pip : 0.0001;
};

export function findPairByCode(code: string) {
  return allPairs.find(pair => pair.code === code);
}